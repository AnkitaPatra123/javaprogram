
package org.StringDemo;
// wap to reverse a string using stringbuilder reverse method
public class StringReverse {
    public static void main(String[] args) {
        String txt="ankita";
        StringBuilder sb=new StringBuilder(txt); // convert string to stringbuilder
        System.out.println("Reverse is:"+sb.reverse()); // reverse string using reverse() method
        //atikna
    }
}
